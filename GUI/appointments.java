    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        SQL obj=new SQL();
        try
        {
            if (jTextField2.getText().equals(""))
            {
                javax.swing.JOptionPane.showMessageDialog(rootPane, "Enter a doctor name please");
            }
            else if (jTextField3.getText().equals(""))
            {
                javax.swing.JOptionPane.showMessageDialog(rootPane, "Enter a patient name please");
            }
            else
            {
                String doctorname=jTextField2.getText();
                String patname=jTextField3.getText();
                List<appointment> app=GlobalVariables.system.getAppointList();
                List<patient> patarr=GlobalVariables.system.getPatientList();

                int patientid=0;
                //System.out.println(patname);

                for(int i=0;i<patarr.size();i++)
                {
                    //System.out.println("THIS THE FOR STAT");
                    if(patarr.get(i).getPatientName() == null ? patname == null : patarr.get(i).getPatientName().equals(patname))
                    {
                        patientid=patarr.get(i).getPid();
                        break;
                    }
                }
                int i=0,aid=0;
                for(;i<app.size();i++)
                {
                    if((app.get(i).getDocname() == null ? doctorname == null : app.get(i).getDocname().equals(doctorname)) && app.get(i).getPatientId()==patientid)
                    {
                        aid=app.get(i).getAppointId();
                        break;
                    }
                }
                appointment appoint=new appointment();
                appoint.setAppointId(aid);
                appoint.setDocname(doctorname);
                appoint.setPatientId(patientid);
                appoint.setTime("0");
                appoint.setToken(-1);

                app.remove(appoint);
                obj.deleteApp(patientid, doctorname);

                javax.swing.JOptionPane.showMessageDialog(rootPane, "The appointment has been rejected");
            }

        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

