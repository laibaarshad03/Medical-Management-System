    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        SQL obj= new SQL();
        try
        {
            if (jTextField2.getText().equals(""))
            {
                javax.swing.JOptionPane.showMessageDialog(rootPane, "Enter a doctor name please");
            }
            else if (jTextField3.getText().equals(""))
            {
                javax.swing.JOptionPane.showMessageDialog(rootPane, "Enter a patient name please");
            }
            else if (jTextField4.getText().equals(""))
            {
                javax.swing.JOptionPane.showMessageDialog(rootPane, "Enter a slot number please");
            }
            else if (jTextField5.getText().equals(""))
            {
                javax.swing.JOptionPane.showMessageDialog(rootPane, "Enter amount for advance payment please");
            }
            else{
                String docname=jTextField2.getText();
                String patname=jTextField3.getText();
                String timeSlotnum=jTextField4.getText(); //sch ID
                String amount=jTextField5.getText(); //advanced payment


                obj.changeStatus(Integer.valueOf(timeSlotnum));

                List<schedule> scharr = GlobalVariables.system.getSchedList();
                int id=0;
                String time="";
                int j=0;
                for(;j<scharr.size();j++)
                {
                    if(scharr.get(j).getSchId()==Integer.valueOf(timeSlotnum))
                    {
                        scharr.get(j).setStatus("");
                        scharr.get(j).setStatus("booked");
                        time=scharr.get(j).getTime();
                        id=scharr.get(j).getDocId();
                        break;
                    }
                }

                List<appointment> app=GlobalVariables.system.getAppointList();
                List<patient> patarr=GlobalVariables.system.getPatientList();

                int pId=0;

                for(int i=0;i<patarr.size();i++)
                {
                    if(patarr.get(i).getPatientName() == null ? patname == null : patarr.get(i).getPatientName().equals(patname))
                    {
                        pId=patarr.get(i).getPid();
                        break;
                    }
                }
                int i=0;
                for(;i<app.size();i++)
                {
                    if((app.get(i).getDocname() == null ? docname == null : app.get(i).getDocname().equals(docname)) && app.get(i).getPatientId()==pId)
                    {
                        app.get(i).setToken((int)(Math.random()*(500-100+1)+100)); //random gener
                        app.get(i).setTime(time);
                        break;
                    }
                }
                //System.out.println(app.get(i).getToken());
                int t=pId+556;
                obj.setToken(docname,pId,t,time);

                obj.setAmount(amount,pId); //db
                List<bills> billarr=GlobalVariables.system.getBillList();

                bills bobj=new bills();
                int bsize=billarr.size();
                //System.out.println("bill size");
                //System.out.println(bsize);
                bobj.setBillId(bsize+1);
                bobj.setPid(pId);
                bobj.setAmount(amount);
                billarr.add(bobj);

                javax.swing.JOptionPane.showMessageDialog(rootPane, "The request has been successfully approved.");
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed
